"""A program to compute addition, subtraction, quotient, multiplication, remainder, floor value, and power when If a = 25, b = 4"""

from math import floor


a = 25
b = 4

print("Addition: ", a+b)
print("Subtraction: ", a-b)
print("Quotient: ", a/b)
print("Multiplication: ", a*b)
print("Remainder: ", a%b)
print("Floor value: ", floor(a/b))
print("Power: ", pow(25,4))